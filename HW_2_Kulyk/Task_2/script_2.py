# Дано дійсне додатнє число a і ціле число n. Обчисліть a в степені n.
# Рішення оформіть у вигляді функції power(a, n). Стандартною
# функцією зведення в степінь або з реалізацією цієї функції в інших
# бібліотеках користуватися не можна. Оператором ** користуватись не
# можна. Реалізувати потрібно циклом

def power(a, n):
    schet = 1
    num_1 = a
    while schet != n:
        a *= num_1
        schet += 1
    return a

print(power(2, 5))
print(power(3, 3))
